package Practice;

public class Programme_Forloop_naturalnumbers {

	public static void main(String[] args) {

		// Print 10 natural numbers

		for (int i = 1; i <= 10; i++) {

			System.out.println("Value of first 10 natural numbers are: " + i);

		}

		System.out.println(" ");

		// Print 10 natural numbers in reverse order

		for (int i = 10; i >= 1; i--) {

			System.out.println("value of first 10 natural number in reverse order is: " + i);

		}

	}

}
